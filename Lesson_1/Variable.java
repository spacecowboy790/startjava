public class Variable {
    public static void main(String[] args) {
        byte countOfGb = 8;
        short countKeysesOfKeyboard = 88;
        int countOfColors = 11_000_000;
        long countOfTransistor = 8_000_000_000L;
        float frequencyOfGpu = 1200.5f;
        double frequencyOfCpu = 3.2d;
        char indexOfCpu = 'i';
        boolean isMyCpuIntel = true;

        System.out.println(countOfGb);
        System.out.println(countKeysesOfKeyboard);
        System.out.println(countOfColors);
        System.out.println(countOfTransistor);
        System.out.println(frequencyOfGpu);
        System.out.println(frequencyOfCpu);
        System.out.println(indexOfCpu);
        System.out.println(isMyCpuIntel);
    }
}